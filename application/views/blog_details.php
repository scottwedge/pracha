function likecounts(id){
	if(id!=''){
		 jQuery.ajax({
					url: "<?php echo site_url('blog/likecount');?>",
					data: {
						postid: id,
					},
					dataType: 'json',
					type: 'POST',
					success: function (data) {
						jQuery('#countids'+id).empty();
						jQuery('#countids'+id).prepend(data.msg);
					
				 }
				});
			}
}
