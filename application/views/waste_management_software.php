						<p>Prachatech has developed the best biomedical waste management software that tracks biomedical waste using barcode system. It is used to track biomedical waste generated from health care facilities to its ultimate disposal. Material tracking can be easily managed through bar coding.  Health care facilities (HCF) generates barcodes for bags or containers containing biomedical waste. For hospitals having less than 30 no. of beds are not required to scan their bags containing bio-medical waste. Truck driver scans the hospital barcode and records the waste collection in app and send the details to HCF (Health Care Facilities) via email. Waste acceptance receipt comprising of date, time, no. of bags, total weight of colour coded bags/containers will be sent to the health care facilities by the CBWTF via email. Whereas, Health Care Facilities (HCF) having 30 or more no. of beds should generate bar codes for all the bags containing bio-medical waste. The waste collected by the truck driver will be sent to the disposal plant. At the disposal plant each bag will be scanned and generates waste acceptance receipt, this acceptance report will be printed and sent to HCF via email. Daily and monthly waste collection reports can be tracked.</p>
